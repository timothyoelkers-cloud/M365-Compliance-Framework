const PowerBIExport = (() => {

  /**
   * Build Power BI compatible schema definition.
   */
  function buildSchema() {
    return {
      tables: [
        {
          name: 'ScanHistory',
          columns: [
            { name: 'ScanId', type: 'string' },
            { name: 'TenantId', type: 'string' },
            { name: 'Timestamp', type: 'datetime' },
            { name: 'Score', type: 'number' },
            { name: 'Configured', type: 'number' },
            { name: 'Missing', type: 'number' },
            { name: 'Manual', type: 'number' },
            { name: 'Total', type: 'number' },
            { name: 'ScanTime', type: 'number' },
            { name: 'EndpointCount', type: 'number' },
          ],
        },
        {
          name: 'PolicyResults',
          columns: [
            { name: 'ScanId', type: 'string' },
            { name: 'PolicyId', type: 'string' },
            { name: 'Status', type: 'string' },
            { name: 'Confidence', type: 'number' },
            { name: 'Detail', type: 'string' },
          ],
        },
        {
          name: 'FrameworkCoverage',
          columns: [
            { name: 'Framework', type: 'string' },
            { name: 'Done', type: 'number' },
            { name: 'Total', type: 'number' },
            { name: 'Percentage', type: 'number' },
          ],
        },
        {
          name: 'GapRegister',
          columns: [
            { name: 'CheckId', type: 'string' },
            { name: 'Name', type: 'string' },
            { name: 'Category', type: 'string' },
            { name: 'Level', type: 'string' },
            { name: 'Priority', type: 'string' },
            { name: 'FrameworkImpact', type: 'number' },
          ],
        },
        {
          name: 'DeploymentHistory',
          columns: [
            { name: 'PolicyId', type: 'string' },
            { name: 'Timestamp', type: 'datetime' },
            { name: 'Status', type: 'string' },
            { name: 'Method', type: 'string' },
            { name: 'Detail', type: 'string' },
          ],
        },
      ],
    };
  }

  /**
   * Export dataset for Power BI.
   * @param {string} format - 'json' or 'csv'
   */
  async function exportDataset(format) {
    format = format || 'json';

    var dataset = {
      _meta: {
        type: 'M365-PowerBI-Dataset',
        version: '1.0',
        exportDate: new Date().toISOString(),
        schema: buildSchema(),
      },
      tables: {},
    };

    // 1. Scan History
    var scanHistory = [];
    if (typeof ScanHistory !== 'undefined') {
      try {
        var scans = await ScanHistory.getScans(AppState.get('authTenantId'), 100);
        scanHistory = scans.map(function (s) {
          var summary = s.summary || {};
          return {
            ScanId: s.id,
            TenantId: s.tenantId || '',
            Timestamp: new Date(s.timestamp).toISOString(),
            Score: s.score || 0,
            Configured: summary.configured || 0,
            Missing: summary.missing || 0,
            Manual: summary.manual || 0,
            Total: summary.total || 0,
            ScanTime: s.scanTime || 0,
            EndpointCount: s.endpointCount || 0,
          };
        });
      } catch (e) {}
    }
    dataset.tables.ScanHistory = scanHistory;

    // 2. Current policy results
    var policyResults = [];
    var scanResults = AppState.get('tenantScanResults') || {};
    for (var pid in scanResults) {
      var r = scanResults[pid];
      policyResults.push({
        ScanId: 'current',
        PolicyId: pid,
        Status: r.status || 'unknown',
        Confidence: r.confidence || 0,
        Detail: r.detail || '',
      });
    }
    dataset.tables.PolicyResults = policyResults;

    // 3. Framework Coverage
    var fwCoverage = AppState.getFrameworkCoverage ? AppState.getFrameworkCoverage() : [];
    dataset.tables.FrameworkCoverage = fwCoverage.map(function (f) {
      return { Framework: f.fw, Done: f.done, Total: f.total, Percentage: f.pct };
    });

    // 4. Gap Register
    var gaps = AppState.getGaps ? AppState.getGaps() : [];
    dataset.tables.GapRegister = gaps.map(function (g) {
      return {
        CheckId: g.id, Name: g.name, Category: g.cat,
        Level: g.level, Priority: g.tier, FrameworkImpact: g.impact,
      };
    });

    // 5. Deployment History
    var deployHistory = AppState.getDeploymentHistory ? AppState.getDeploymentHistory() : [];
    dataset.tables.DeploymentHistory = deployHistory.slice(0, 200).map(function (d) {
      return {
        PolicyId: d.policyId, Timestamp: new Date(d.timestamp).toISOString(),
        Status: d.status, Method: d.method || '', Detail: d.detail || '',
      };
    });

    // Export
    if (format === 'csv') {
      exportAsCSV(dataset);
    } else {
      exportAsJSON(dataset);
    }

    if (typeof AuditTrail !== 'undefined') {
      AuditTrail.log('export.powerbi', 'Exported Power BI dataset (' + format + ')');
    }
  }

  function exportAsJSON(dataset) {
    var content = JSON.stringify(dataset, null, 2);
    var blob = new Blob([content], { type: 'application/json' });
    downloadBlob(blob, 'M365-PowerBI-' + new Date().toISOString().replace(/[:.]/g, '-') + '.json');
    showToast('Power BI dataset exported (JSON)');
  }

  function exportAsCSV(dataset) {
    // Export each table as a separate CSV section
    var content = '';
    for (var tableName in dataset.tables) {
      var rows = dataset.tables[tableName];
      if (rows.length === 0) continue;

      content += '# Table: ' + tableName + '\n';
      var headers = Object.keys(rows[0]);
      content += headers.join(',') + '\n';

      for (var i = 0; i < rows.length; i++) {
        var line = headers.map(function (h) {
          var val = rows[i][h];
          if (val === null || val === undefined) return '';
          var s = String(val);
          if (s.indexOf(',') !== -1 || s.indexOf('"') !== -1 || s.indexOf('\n') !== -1) {
            return '"' + s.replace(/"/g, '""') + '"';
          }
          return s;
        });
        content += line.join(',') + '\n';
      }
      content += '\n';
    }

    var blob = new Blob([content], { type: 'text/csv' });
    downloadBlob(blob, 'M365-PowerBI-' + new Date().toISOString().replace(/[:.]/g, '-') + '.csv');
    showToast('Power BI dataset exported (CSV)');
  }

  function downloadBlob(blob, filename) {
    var url = URL.createObjectURL(blob);
    var a = document.createElement('a');
    a.href = url;
    a.download = filename;
    a.click();
    URL.revokeObjectURL(url);
  }

  /**
   * Render export button with format picker.
   */
  function renderExportPanel() {
    var html = '<div style="display:flex;gap:6px;align-items:center">';
    html += '<button class="btn btn-sm" onclick="PowerBIExport.exportDataset(\'json\')" style="font-size:.62rem">Power BI (JSON)</button>';
    html += '<button class="btn btn-sm" onclick="PowerBIExport.exportDataset(\'csv\')" style="font-size:.62rem">Power BI (CSV)</button>';
    html += '</div>';
    return html;
  }

  return {
    buildSchema: buildSchema,
    exportDataset: exportDataset,
    renderExportPanel: renderExportPanel,
  };
})();
