// ─────────────────────────────────────────
// WORK ITEM INTEGRATION
// ─────────────────────────────────────────
const WorkItemIntegration = (() => {
  const SETTINGS_KEY = 'm365-workitem-settings';

  function getSettings() {
    try {
      var raw = localStorage.getItem(SETTINGS_KEY);
      return raw ? JSON.parse(raw) : null;
    } catch (e) { return null; }
  }

  function saveSettings(settings) {
    try {
      localStorage.setItem(SETTINGS_KEY, JSON.stringify(settings));
    } catch (e) {}
  }

  /**
   * Create a single GitHub Issue from a gap.
   */
  async function createGitHubIssue(gap, settings) {
    var parts = (settings.repo || '').split('/');
    if (parts.length !== 2) throw new Error('Invalid repository format. Use owner/repo');

    var body = '## Compliance Gap: ' + (gap.id || '') + '\n\n';
    body += '**Control:** ' + (gap.name || '') + '\n';
    body += '**Category:** ' + (gap.cat || '') + '\n';
    body += '**Level:** ' + (gap.level || '') + '\n';
    body += '**Priority:** ' + (gap.tier || '') + '\n';
    body += '**Framework Impact:** ' + (gap.impact || 0) + ' frameworks\n\n';
    body += '---\n*Generated by M365 Compliance Framework*';

    var response = await fetch('https://api.github.com/repos/' + parts[0] + '/' + parts[1] + '/issues', {
      method: 'POST',
      headers: {
        'Authorization': 'token ' + settings.token,
        'Content-Type': 'application/json',
        'Accept': 'application/vnd.github.v3+json',
      },
      body: JSON.stringify({
        title: '[Compliance Gap] ' + (gap.id || '') + ': ' + (gap.name || '').substring(0, 80),
        body: body,
        labels: ['compliance', gap.tier || 'medium'],
      }),
    });

    if (!response.ok) {
      var errBody = await response.text();
      throw new Error('GitHub API error: ' + response.status + ' - ' + errBody);
    }
    return await response.json();
  }

  /**
   * Create a single Azure DevOps work item from a gap.
   */
  async function createAzDoWorkItem(gap, settings) {
    var org = settings.org;
    var project = settings.project;
    if (!org || !project) throw new Error('Organization and project are required');

    var url = 'https://dev.azure.com/' + encodeURIComponent(org) + '/' +
      encodeURIComponent(project) + '/_apis/wit/workitems/$Task?api-version=7.0';

    var body = [
      { op: 'add', path: '/fields/System.Title', value: '[Compliance Gap] ' + (gap.id || '') + ': ' + (gap.name || '').substring(0, 120) },
      { op: 'add', path: '/fields/System.Description', value: '<strong>Category:</strong> ' + (gap.cat || '') + '<br><strong>Level:</strong> ' + (gap.level || '') + '<br><strong>Priority:</strong> ' + (gap.tier || '') + '<br><strong>Framework Impact:</strong> ' + (gap.impact || 0) + ' frameworks' },
      { op: 'add', path: '/fields/System.Tags', value: 'compliance;' + (gap.tier || 'medium') },
    ];

    // Map priority
    var priorityMap = { critical: 1, high: 2, medium: 3, low: 4 };
    var priority = priorityMap[gap.tier] || 3;
    body.push({ op: 'add', path: '/fields/Microsoft.VSTS.Common.Priority', value: priority });

    var response = await fetch(url, {
      method: 'POST',
      headers: {
        'Authorization': 'Basic ' + btoa(':' + settings.token),
        'Content-Type': 'application/json-patch+json',
      },
      body: JSON.stringify(body),
    });

    if (!response.ok) {
      var errBody = await response.text();
      throw new Error('Azure DevOps API error: ' + response.status + ' - ' + errBody);
    }
    return await response.json();
  }

  /**
   * Create a single work item.
   */
  async function createWorkItem(gap) {
    var settings = getSettings();
    if (!settings || !settings.token) throw new Error('Integration not configured');

    if (settings.provider === 'github') {
      return createGitHubIssue(gap, settings);
    } else if (settings.provider === 'azdo') {
      return createAzDoWorkItem(gap, settings);
    } else {
      throw new Error('Unknown provider: ' + settings.provider);
    }
  }

  /**
   * Bulk create work items from gap register entries.
   */
  async function bulkCreate(gaps) {
    var settings = getSettings();
    if (!settings || !settings.token) {
      showToast('Please configure integration settings first');
      return { created: 0, errors: [] };
    }

    var created = 0;
    var errors = [];

    for (var i = 0; i < gaps.length; i++) {
      try {
        await createWorkItem(gaps[i]);
        created++;
        showToast('Created ' + created + '/' + gaps.length + ' work items...');

        // Rate limiting for GitHub (1 per second)
        if (settings.provider === 'github' && i < gaps.length - 1) {
          await new Promise(function (r) { setTimeout(r, 1100); });
        }
      } catch (e) {
        errors.push({ gap: gaps[i].id, error: e.message });
      }
    }

    if (typeof AuditTrail !== 'undefined') {
      AuditTrail.log('integration.workitems', 'Created ' + created + ' work items', {
        provider: settings.provider,
        total: gaps.length,
        created: created,
        errors: errors.length,
      });
    }

    showToast('Created ' + created + ' work items' + (errors.length > 0 ? ' (' + errors.length + ' errors)' : ''));
    return { created: created, errors: errors };
  }

  /**
   * Render settings modal.
   */
  function renderSettingsModal() {
    var settings = getSettings() || { provider: 'github', token: '', repo: '', org: '', project: '' };
    var overlay = document.getElementById('modal-overlay');
    var modal = document.getElementById('modal');
    if (!overlay || !modal) return;

    var html = '<div class="modal-header">';
    html += '<h3>Work Item Integration</h3>';
    html += '<button class="modal-close" onclick="document.getElementById(\'modal-overlay\').classList.remove(\'open\')">&times;</button>';
    html += '</div>';
    html += '<div class="modal-body">';

    html += '<div style="font-size:.68rem;color:var(--amber);background:var(--amber-lt);padding:8px;border-radius:6px;margin-bottom:12px">';
    html += '&#9888; Personal Access Tokens are sensitive credentials. Only use in trusted environments.</div>';

    html += '<div class="form-row" style="margin-bottom:12px">';
    html += '<label style="font-size:.72rem;font-weight:600;display:block;margin-bottom:4px">Provider</label>';
    html += '<select id="wi-provider" style="font-size:.72rem;padding:4px 8px;border:1px solid var(--border);border-radius:6px;background:var(--bg2)" onchange="WorkItemIntegration._toggleProvider()">';
    html += '<option value="github" ' + (settings.provider === 'github' ? 'selected' : '') + '>GitHub Issues</option>';
    html += '<option value="azdo" ' + (settings.provider === 'azdo' ? 'selected' : '') + '>Azure DevOps</option>';
    html += '</select></div>';

    html += '<div class="form-row" style="margin-bottom:12px">';
    html += '<label style="font-size:.72rem;font-weight:600;display:block;margin-bottom:4px">Personal Access Token</label>';
    html += '<input type="password" id="wi-token" value="' + escHtml(settings.token) + '" placeholder="ghp_... or PAT" style="width:100%;font-size:.72rem;padding:6px 8px;border:1px solid var(--border);border-radius:6px;background:var(--bg2)">';
    html += '</div>';

    // GitHub fields
    html += '<div id="wi-github-fields" style="' + (settings.provider !== 'github' ? 'display:none' : '') + '">';
    html += '<div class="form-row" style="margin-bottom:12px">';
    html += '<label style="font-size:.72rem;font-weight:600;display:block;margin-bottom:4px">Repository (owner/repo)</label>';
    html += '<input type="text" id="wi-repo" value="' + escHtml(settings.repo || '') + '" placeholder="org/repo-name" style="width:100%;font-size:.72rem;padding:6px 8px;border:1px solid var(--border);border-radius:6px;background:var(--bg2)">';
    html += '</div></div>';

    // AzDO fields
    html += '<div id="wi-azdo-fields" style="' + (settings.provider !== 'azdo' ? 'display:none' : '') + '">';
    html += '<div class="form-row" style="margin-bottom:12px;display:flex;gap:12px">';
    html += '<div style="flex:1"><label style="font-size:.72rem;font-weight:600;display:block;margin-bottom:4px">Organization</label>';
    html += '<input type="text" id="wi-org" value="' + escHtml(settings.org || '') + '" placeholder="myorg" style="width:100%;font-size:.72rem;padding:6px 8px;border:1px solid var(--border);border-radius:6px;background:var(--bg2)"></div>';
    html += '<div style="flex:1"><label style="font-size:.72rem;font-weight:600;display:block;margin-bottom:4px">Project</label>';
    html += '<input type="text" id="wi-project" value="' + escHtml(settings.project || '') + '" placeholder="MyProject" style="width:100%;font-size:.72rem;padding:6px 8px;border:1px solid var(--border);border-radius:6px;background:var(--bg2)"></div>';
    html += '</div></div>';

    html += '<div class="config-actions">';
    html += '<button class="btn btn-primary" onclick="WorkItemIntegration._save()">Save</button> ';
    html += '<button class="btn" onclick="document.getElementById(\'modal-overlay\').classList.remove(\'open\')">Cancel</button>';
    html += '</div></div>';

    modal.innerHTML = html;
    overlay.classList.add('open');
  }

  function _toggleProvider() {
    var provider = document.getElementById('wi-provider').value;
    document.getElementById('wi-github-fields').style.display = provider === 'github' ? '' : 'none';
    document.getElementById('wi-azdo-fields').style.display = provider === 'azdo' ? '' : 'none';
  }

  function _save() {
    var provider = document.getElementById('wi-provider').value;
    var token = document.getElementById('wi-token').value.trim();
    var settings = { provider: provider, token: token };

    if (provider === 'github') {
      settings.repo = document.getElementById('wi-repo').value.trim();
    } else {
      settings.org = document.getElementById('wi-org').value.trim();
      settings.project = document.getElementById('wi-project').value.trim();
    }

    saveSettings(settings);
    showToast('Integration settings saved');
    document.getElementById('modal-overlay').classList.remove('open');
  }

  /**
   * Render "Create Work Items" button for gap register.
   */
  function renderCreateButton(gaps) {
    if (!gaps || gaps.length === 0) return '';
    var settings = getSettings();
    var configured = settings && settings.token;

    var html = '<div style="display:flex;gap:8px;margin-top:8px;align-items:center">';
    if (configured) {
      html += '<button class="btn btn-sm" onclick="WorkItemIntegration._bulkFromGaps()" style="font-size:.62rem">Create Work Items (' + gaps.length + ')</button>';
    }
    html += '<button class="btn btn-sm" onclick="WorkItemIntegration.renderSettingsModal()" style="font-size:.62rem" title="Configure integration">&#9881; Integration Settings</button>';
    html += '</div>';
    return html;
  }

  async function _bulkFromGaps() {
    var gaps = AppState.getGaps();
    if (gaps.length === 0) { showToast('No gaps to create work items for'); return; }
    if (!confirm('Create ' + gaps.length + ' work items? This may take a moment.')) return;
    await bulkCreate(gaps);
  }

  return {
    getSettings: getSettings,
    saveSettings: saveSettings,
    createWorkItem: createWorkItem,
    bulkCreate: bulkCreate,
    renderSettingsModal: renderSettingsModal,
    renderCreateButton: renderCreateButton,
    _toggleProvider: _toggleProvider,
    _save: _save,
    _bulkFromGaps: _bulkFromGaps,
  };
})();


